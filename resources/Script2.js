/**
     * @author : Angappan
     * @function_description : Validate the Multiplication functionality 
     * @input Params : Number, Number
     * @return : Product of number1 & number2
    **/
export async function validateMultiplication(num1, num2) {
    return num1 * num2
}

/**
     * @author : Angappan
     * @function_description : Validate the Division functionality 
     * @input Params : Number, Number
     * @return : Quotient of number1 & number2
    **/
export async function validateDivision(num1, num2) {
    return num1 / num2
}